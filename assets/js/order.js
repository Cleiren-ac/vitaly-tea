function getDetails(){

    var customer_name = document.getElementById("info-name").value;
    var customer_address = document.getElementById("info-add").value;
    var customer_number = document.getElementById("info-num").value;
    var total_bill = document.getElementById("amt").value;
    var customer_payment = document.getElementById("pay").value;

    var payment = parseInt(customer_payment);
    var cost = parseInt(total_bill);
    var change = payment - cost;

    if(payment < cost || customer_name == "" || customer_address == "" || customer_number == ""){
        document.getElementById("msg").innerHTML = "Transaction failed.";
        document.getElementById("msg").style.color = "red";
        document.getElementById("continue").disabled = true;
    } else {
        document.getElementById("msg").innerHTML = "Double check your details.";
        document.getElementById("msg").style.color = "black";
        document.getElementById("continue").disabled = false;
        document.getElementById("cus-name").innerHTML = customer_name;
        document.getElementById("cus-add").innerHTML = customer_address;
        document.getElementById("cus-num").innerHTML = customer_number;
        document.getElementById("cus-bill").innerHTML = total_bill;
        document.getElementById("cus-pay").innerHTML = payment;
        document.getElementById("cus-change").innerHTML = change;
    }
}

